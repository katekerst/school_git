#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *c1;

	c1 = NULL;
	while (*s != '\0')
	{
		if (*s == (char)c)
			c1 = (char *)s;
		s++;
	}
	if (c == '\0')
		return ((char*)s);
	return (c1);
}
